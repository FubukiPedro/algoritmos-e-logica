
# Aspas simples
print('Pedro Henrique')
print('"Pedro Henrique"')

# Aspas duplas
print("Shirou Fubuki")
print(27, "'Shirou Fubuki'")

# Escape (esse "\" é tipo um "Calma, isso aqui não é o fim da string, é só um caractere especial."")
print("Pedro \"Fubuki\" Henrique")

# r (O r significa raw string (string crua).)
# "Não interprete \ como escape. Mostra exatamente como está."

print(r"C:\novo\teste")
# Sem o r, o \n viraria uma quebra de linha e o \t viraria um tab
print(r"Pedro \"Fubuki\" Henrique")

# str (string) -> texto
# String são textos, ou seja, palavras, frases, etc. Para criar uma string, basta colocar o texto entre aspas simples ou aspas duplas.
