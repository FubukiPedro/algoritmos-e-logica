# int e float

# int -> inteiro
# int é qualquer numero se ., seja ele negativo ou positivo
# exemplo: -10, -8, 0, 5, 9...
print(-10, -8)
print(0, 5, 9)

print()

# float -> real
# float é qualquer numero com ponto flutuante
#exemplo: 
print(-10.6, -8.2)
print(0.2, 5.3, 9.7)

print()

# A função type mostra o tipo que o Python
# inferiu ao valor.
print(type('Pedro'))
print(type(0))
print(type(-1.5), type(0.0), type(5.4))
print(type(True))